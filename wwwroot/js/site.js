var map = L.map('map').setView([51.505, -0.09], 13);

L.tileLayer('https://{s}.tile.openstreetmap.org/{z}/{x}/{y}.png', {
    attribution: '© OpenStreetMap contributors'
}).addTo(map);

map.on('click', function (e) {
    var latlng = e.latlng;
    alert("Coordinates: " + latlng.lat + ", " + latlng.lng);
});